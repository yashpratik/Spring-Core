package com.Assignment_11;


public class Student 
{
	String sname;
	int rollno;
	String dob;
	String cname;
	String section;
	Result res;
	
	public Student(String sname, int rollno, String dob, String cname, String section, Result res) {
		super();
		this.sname = sname;
		this.rollno = rollno;
		this.dob = dob;
		this.cname = cname;
		this.section = section;
		this.res = res;
	}
	
}
